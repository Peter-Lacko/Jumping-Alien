public enum MovementDirection {

	LEFT {
		public char getSymbol() {
			return 'L';
		}
	},
	RIGHT {
		public char getSymbol() {
			return 'R';
		}
	},
	NONE {
		public char getSymbol(){
			return 'N';
		}
	};
	
	public abstract char getSymbol();
}
